for column in seasons:
    max_temp[column] = pd.to_numeric(max_temp[column], errors='coerce')
    min_temp[column] = pd.to_numeric(min_temp[column], errors='coerce')
    mean_temp[column] = pd.to_numeric(mean_temp[column], errors='coerce')

